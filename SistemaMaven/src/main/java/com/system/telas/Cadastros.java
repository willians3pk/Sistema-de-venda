    private void CadastroItem() {

        String tamanho = null;
        if (camp_nomeItem.getText().length() > 0) {
            jLabelObrigatorioVenda.setVisible(false);
        } else {
            jLabelObrigatorioVenda.setVisible(true);
        }
        if (camp_codigo.getText().length() > 0) {
            jLabelObrigatorioQnt.setVisible(false);
        } else {
            jLabelObrigatorioQnt.setVisible(true);
        }
        if (camp_qnt.getText().length() > 0) {
            jLabelObrigatorioQnt.setVisible(false);
        } else {
            jLabelObrigatorioQnt.setVisible(true);
        }
        if (camp_valorCompra.getText().length() > 0) {
            jLabelObrigatorioCompra.setVisible(false);
        } else {
            jLabelObrigatorioCompra.setVisible(true);
        }
        if (camp_valorVenda.getText().length() > 0) {
            jLabelObrigatorioVenda.setVisible(false);
        } else {
            jLabelObrigatorioVenda.setVisible(true);
        }

        if ((camp_nomeItem.getText().length() > 0)
                && (camp_codigo.getText().length() > 0)
                && (camp_qnt.getText().length() > 0)
                && (camp_valorCompra.getText().length() > 0)
                && (camp_valorVenda.getText().length() > 0)) {

            Items item = new Items();
            Long precoCompra = Long.parseLong(camp_valorCompra.getText().replaceAll(",", ""));//remove a virgula e adiciona apenas os numeros decimais
            Long precoVenda = Long.parseLong(camp_valorVenda.getText().replaceAll(",", "")); //remove a virgula e adiciona apenas os numeros decimais
            Long codigo = Long.parseLong(camp_codigo.getText());
            int quantidade = Integer.parseInt(camp_qnt.getText());
            int valorTotal = (int) (precoVenda * quantidade);

//          -- AINDA ESTÁ FALTANDO ACRESCENTAR O FORNECEDOR --
            item.setItem(camp_nomeItem.getText());
            item.setValor_compra(precoCompra);
            item.setValor_venda(precoVenda);
            item.setCodigo(codigo);
//            boolean status = chekBoxAtivado.isSelected(); // LEMBRA DE REMOVER A CHEKBOX E USAR O CAMPO STATUS APENAS PARA ITEMS EXCLUIDOS
            item.setStatus(true);
            item.setExcluido(false);
            item.setQnt(quantidade);
            item.setDescricao(camp_descricao.getText());
            item.setValor_total(valorTotal);

            if (comBox_tamanho.getSelectedItem().toString().equals("<selecione>")) {
                JOptionPane.showMessageDialog(null, "Selecione o Tamanho!!");
            } else {

                tamanho = comBox_tamanho.getSelectedItem().toString();
                item.setTamanho(tamanho);
                Conexao banco = new Conexao();
                banco.save(item);

                camp_nomeItem.setText("");
                camp_codigo.setText("");
                camp_valorCompra.setText("");
                camp_valorVenda.setText("");
                camp_qnt.setText("");
                camp_descricao.setText("");

            }
        } else {
            JOptionPane.showMessageDialog(null, "Confira os campos Obrigatórios!!");
        }

    }

    private void CadastroPessoa() {

        Conexao banco = new Conexao();

        if ((camp_nomePessoa.getText().length() > 0) && (camp_cpfPessoa.getText().length() > 0)
                && (camp_emailPessoa.getText().length() > 0) && (camp_dataNasc.getText().length() > 0)) {

            Pessoa pessoa = new Pessoa();
            pessoa.setNome(camp_nomePessoa.getText());
            int cpf = Integer.parseInt(camp_cpfPessoa.getText());
            pessoa.setCpf(cpf);
            pessoa.setEmail(camp_emailPessoa.getText());
            pessoa.setDataNascimento(new Date(camp_dataNasc.getText()));

// ---------- ENDEREÇO
            Endereco end = new Endereco();

            if ((camp_ruaPessoa.getText().length() > 0) && (camp_cepPessoa.getText().length() > 0)
                    && (camp_bairroPessoa.getText().length() > 0) && (camp_cidadePessoa.getText().length() > 0)
                    && (camp_numerocasaPessoa.getText().length() > 0)) {

                end.setRua(camp_ruaPessoa.getText());
                end.setCidade(camp_cidadePessoa.getText());
                int cep = Integer.parseInt(camp_cepPessoa.getText());
                end.setCep(cep);
                end.setComplemento(camp_complementoPessoa.getText());
                int numeroCasa = Integer.parseInt(camp_numerocasaPessoa.getText());
                end.setNumeroCasa(numeroCasa);
//              end.setPessoas(pessoas);
                banco.persist(end);
            }

            pessoa.setEndereco(end);

            banco.persist(pessoa);

//            LIMPA OS CAMPOS DO FORMULARIO
            camp_nomePessoa.setText("");
            camp_cpfPessoa.setText("");
            camp_emailPessoa.setText("");
            camp_dataNasc.setText("");
            camp_ruaPessoa.setText("");
            camp_cepPessoa.setText("");
            camp_bairroPessoa.setText("");
            camp_numerocasaPessoa.setText("");
            camp_cidadePessoa.setText("");
            camp_complementoPessoa.setText("");

        } else {
            JOptionPane.showMessageDialog(null, "Preencha as Informações relevantes");
        }

    }

    private void CadastroFornecedor() {

        Fornecedor forne = new Fornecedor();
        Endereco end = new Endereco();
        NumeroContato contato = new NumeroContato();

        int cnpj = Integer.parseInt(camp_cnpj.getText());
        int cpf = Integer.parseInt(camp_cpfPessoa.getText());

        forne.setNome(camp_nomeFornecedor.getText());
        forne.setCnpj(cnpj);
        forne.setEmail(camp_emailFornecedor.getText());
        forne.setHomePage(camp_homepage.getText());
        forne.setCpf(cpf);

        //---------------------------------------------------------------------//
        int ddd = Integer.parseInt(camp_dddFornecedor.getText());
        int numeroContato = Integer.parseInt(camp_contatoFornecedor.getText());

        // -- numero de contato --
        contato.setDdd(ddd);
        contato.setFone(numeroContato);
        contato.setFornecedor(forne);
        contato.setCpf(cpf);

        int cep = Integer.parseInt(camp_cepFornecedor.getText());
        int numeroCasa = Integer.parseInt(camp_numerocasaFornecedor.getText());

        end.setRua(camp_ruaFornecedor.getText());
        end.setCep(cep);
        end.setNumeroCasa(numeroCasa);
        end.setBairro(camp_bairroFornecedor.getText());
        end.setEstadoUf(camp_EstadoFornecedor.getText());
        end.setComplemento(camp_complementoForncedor.getText());

        forne.setEndereco(end);
        Conexao banco = new Conexao();
        banco.save(end);
        banco.persist(forne);
        banco.save(contato);

    }
