    private void btn_okActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_okActionPerformed
        DefaultTableModel tableDefault = (DefaultTableModel) tabela_cliente.getModel();
        int linha = tabela_cliente.getSelectedRow();
        for (int i = 0; i < banco.list_Cliente().size(); i++) {
            if (banco.list_Cliente().get(i).getIdpessoa().equals(tableDefault.getValueAt(linha, 0))) {// VERIFICA SE O ID DO OBJETO CONTEM NO BANCO DE DADOS
                client = banco.list_Cliente().get(i); // pega o produto da lista do banco de dados;
                TelaVenda.camp_nomeCliente.setText(client.getNome());
                TelaVenda.setCliente(client);
                dispose();
            }
        }

    }//GEN-LAST:event_btn_okActionPerformed

