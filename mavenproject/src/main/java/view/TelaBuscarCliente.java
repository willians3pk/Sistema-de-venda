    private void camp_nomeKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_camp_nomeKeyReleased
        btn_ok.setEnabled(false);
        String pesquisa = camp_nome.getText();
        List<Cliente> ListaCliente = new ArrayList<Cliente>();
        
        for (int i = 1; i < banco.list_Cliente().size(); i++) {
            // VERIFICA SE O NOME COMTEM NA LISTA DE CLIENTE E VERIFICA O STATUS DO CLIENTE;
            if (banco.list_Cliente().get(i).getNome().contains(pesquisa) && banco.list_Cliente().get(i).isStatus()) {
                Cliente c = banco.list_Cliente().get(i);
                ListaCliente.add(c); // ADICIONA NA LISTA CLIENTE;
            }
        }

        DefaultTableModel tabela = (DefaultTableModel) tabela_cliente.getModel();
        Locale localeBR = new Locale("pt", "BR"); //declaração da variável do tipo Locale, responsável por definir o idioma e localidade a serem utilizados nas formatações;
        NumberFormat dinheiro = NumberFormat.getCurrencyInstance(localeBR);
        try {
            tabela.setNumRows(0);
            for (Cliente p : ListaCliente) {
                tabela.addRow(new Object[]{p.getIdpessoa(), p.getNome(), p.getCpf()});
            }
        } catch (Exception e) {
            System.out.println(e);
            JOptionPane.showMessageDialog(null, "Erro no Codigo");
        }
    }//GEN-LAST:event_camp_nomeKeyReleased

